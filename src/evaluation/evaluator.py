import json
from pathlib import Path
from typing import Dict, Any, List


class SwingEvaluator:
    """
    Evaluates whether the v1 swing analysis pipeline produced believable,
    inspectable, and well-documented results.

    This class does not calculate new swing mechanics.
    It summarizes and checks the outputs from earlier modules.
    """

    def __init__(
        self,
        sample_name: str,
        events: Dict[str, int],
        features: Dict[str, Any],
        feedback_output: Dict[str, Any],
        visualization_dir: str,
        output_report_path: str,
    ):
        """
        Initialize the evaluator.

        Args:
            sample_name:
                Name of the swing sample being evaluated.

            events:
                Dictionary of detected swing events from Module 4.

            features:
                Dictionary of extracted swing features from Module 5.

            feedback_output:
                Dictionary of feedback generated by Module 6.

            visualization_dir:
                Folder containing Module 7 visualization outputs.

            output_report_path:
                Path where the evaluation markdown report should be saved.
        """
        self.sample_name = sample_name
        self.events = events
        self.features = features
        self.feedback_output = feedback_output
        self.visualization_dir = Path(visualization_dir)
        self.output_report_path = Path(output_report_path)

        self.evaluation_notes: List[str] = []
        self.failure_notes: List[str] = []

    def check_event_order(self) -> None:
        """
        Check whether detected swing events occur in a logical order.
        """
        movement_start = self.events.get("movement_start")
        peak_hand_speed = self.events.get("peak_hand_speed")
        contact_proxy = self.events.get("contact_proxy")

        if movement_start is None or peak_hand_speed is None or contact_proxy is None:
            self.failure_notes.append(
                "One or more required swing events are missing."
            )
            return

        if movement_start < peak_hand_speed < contact_proxy:
            self.evaluation_notes.append(
                "Detected events occur in a believable order: "
                "movement start → peak hand speed → contact proxy."
            )
        else:
            self.failure_notes.append(
                "Detected events do not occur in the expected order. "
                "This should be visually reviewed."
            )

    def check_feature_values(self) -> None:
        """
        Check whether key feature values exist and are directionally usable.
        """
        required_features = [
            "head_movement_start_to_contact",
            "hand_path_start_to_contact",
            "hip_drift_start_to_contact",
            "shoulder_angle_change_start_to_contact",
            "frames_start_to_contact",
            "frames_start_to_peak_hand_speed",
            "frames_peak_hand_speed_to_contact",
        ]

        missing_features = []

        for feature_name in required_features:
            if feature_name not in self.features:
                missing_features.append(feature_name)

        if missing_features:
            self.failure_notes.append(
                "Missing required feature values: "
                + ", ".join(missing_features)
            )
            return

        self.evaluation_notes.append(
            "All required v1 feature values were present in the feature output."
        )

        head_movement = float(self.features["head_movement_start_to_contact"])
        hand_path = float(self.features["hand_path_start_to_contact"])
        shoulder_angle = float(
            self.features["shoulder_angle_change_start_to_contact"]
        )
        frames_start_to_contact = int(self.features["frames_start_to_contact"])

        if head_movement < hand_path:
            self.evaluation_notes.append(
                "Head movement was lower than hand path distance, which is "
                "directionally believable for a baseball swing."
            )
        else:
            self.failure_notes.append(
                "Head movement was not lower than hand path distance. "
                "This may indicate noisy landmarks or a feature calculation issue."
            )

        if 15.0 <= shoulder_angle <= 80.0:
            self.evaluation_notes.append(
                "Shoulder angle change was within a broad believable v1 range."
            )
        else:
            self.failure_notes.append(
                "Shoulder angle change was outside the broad v1 expected range."
            )

        if 8 <= frames_start_to_contact <= 35:
            self.evaluation_notes.append(
                "Movement start to contact proxy timing was within a broad "
                "believable v1 frame range."
            )
        else:
            self.failure_notes.append(
                "Movement start to contact proxy timing was outside the broad "
                "v1 expected range."
            )

    def check_feedback_output(self) -> None:
        """
        Check whether feedback output contains expected structure.
        """
        expected_keys = ["summary", "feedback", "warnings"]

        missing_keys = []

        for key in expected_keys:
            if key not in self.feedback_output:
                missing_keys.append(key)

        if missing_keys:
            self.failure_notes.append(
                "Feedback output is missing expected keys: "
                + ", ".join(missing_keys)
            )
            return

        feedback_items = self.feedback_output["feedback"]
        warning_items = self.feedback_output["warnings"]

        if len(feedback_items) > 0:
            self.evaluation_notes.append(
                "Feedback engine produced at least one feedback statement."
            )
        else:
            self.failure_notes.append(
                "Feedback engine did not produce any feedback statements."
            )

        if len(warning_items) > 0:
            self.evaluation_notes.append(
                "Feedback engine included warning notes for known v1 limitations."
            )
        else:
            self.failure_notes.append(
                "Feedback engine did not include any warning notes."
            )

    def check_visualization_outputs(self) -> None:
        """
        Check whether expected Module 7 visualization files exist.
        """
        expected_files = [
            "key_frame_movement_start.jpg",
            "key_frame_peak_hand_speed.jpg",
            "key_frame_contact_proxy.jpg",
            "hand_path_plot.png",
            "head_path_plot.png",
            "feature_summary.png",
            "timing_events.png",
        ]

        missing_files = []

        for filename in expected_files:
            file_path = self.visualization_dir / filename

            if not file_path.exists():
                missing_files.append(filename)

        if missing_files:
            self.failure_notes.append(
                "Missing expected visualization files: "
                + ", ".join(missing_files)
            )
        else:
            self.evaluation_notes.append(
                "All expected Module 7 visualization files were created."
            )

    def add_known_limitations(self) -> List[str]:
        """
        Return known v1 limitations that should be documented in the report.

        Returns:
            List of limitation strings.
        """
        return [
            (
                "This evaluation uses one sample video, so it does not prove "
                "general performance across multiple hitters, camera angles, "
                "or video qualities."
            ),
            (
                "The contact frame is still a proxy because the bat and ball "
                "are not tracked in v1."
            ),
            (
                "Distance metrics are in normalized landmark coordinate units, "
                "not inches or centimeters."
            ),
            (
                "Hip drift is not reliable yet because the current cleaned "
                "landmark file was normalized around the hip center."
            ),
            (
                "Feedback thresholds are hardcoded v1 placeholders and should "
                "later be refined using more swing samples."
            ),
            (
                "MediaPipe landmarks can jitter during fast motion, blur, or "
                "partial occlusion."
            ),
        ]

    def run_evaluation_checks(self) -> None:
        """
        Run all v1 evaluation checks.
        """
        self.check_event_order()
        self.check_feature_values()
        self.check_feedback_output()
        self.check_visualization_outputs()

    def build_markdown_report(self) -> str:
        """
        Build a markdown evaluation report.

        Returns:
            Markdown report string.
        """
        limitations = self.add_known_limitations()

        report_lines = [
            f"# V1 Evaluation Report: {self.sample_name}",
            "",
            "## Purpose",
            "",
            (
                "This report evaluates whether the v1 baseball swing analysis "
                "pipeline produced believable and inspectable results for the "
                "current sample video."
            ),
            "",
            "This is a single-sample evaluation, not a full dataset validation.",
            "",
            "---",
            "",
            "## Pipeline Status",
            "",
            "The v1 pipeline includes:",
            "",
            "```text",
            "video ingestion",
            "pose extraction",
            "landmark processing",
            "event detection",
            "feature extraction",
            "feedback generation",
            "visualization output",
            "evaluation report",
            "```",
            "",
            "---",
            "",
            "## Detected Events",
            "",
            "```json",
            json.dumps(self.events, indent=4),
            "```",
            "",
            "---",
            "",
            "## Extracted Features",
            "",
            "```json",
            json.dumps(self.features, indent=4),
            "```",
            "",
            "---",
            "",
            "## Feedback Summary",
            "",
            self.feedback_output.get("summary", "No summary available."),
            "",
            "### Feedback Items",
            "",
        ]

        for item in self.feedback_output.get("feedback", []):
            report_lines.extend(
                [
                    f"#### {item.get('metric', 'unknown_metric')}",
                    "",
                    f"- Status: {item.get('status', 'unknown')}",
                    f"- Statement: {item.get('statement', '')}",
                    f"- Evidence: {item.get('evidence', '')}",
                    f"- Confidence: {item.get('confidence', 'unknown')}",
                    "",
                ]
            )

        report_lines.extend(
            [
                "---",
                "",
                "## Evaluation Notes",
                "",
            ]
        )

        if self.evaluation_notes:
            for note in self.evaluation_notes:
                report_lines.append(f"- {note}")
        else:
            report_lines.append("- No positive evaluation notes were recorded.")

        report_lines.extend(
            [
                "",
                "---",
                "",
                "## Failure / Review Notes",
                "",
            ]
        )

        if self.failure_notes:
            for note in self.failure_notes:
                report_lines.append(f"- {note}")
        else:
            report_lines.append(
                "- No major failure notes were recorded during this v1 evaluation."
            )

        report_lines.extend(
            [
                "",
                "---",
                "",
                "## Known Limitations",
                "",
            ]
        )

        for limitation in limitations:
            report_lines.append(f"- {limitation}")

        report_lines.extend(
            [
                "",
                "---",
                "",
                "## Evaluation Conclusion",
                "",
                (
                    "The v1 pipeline successfully runs end-to-end on the current "
                    "Mike Trout sample video and produces detected events, "
                    "feature values, feedback statements, visualization assets, "
                    "and an evaluation report."
                ),
                "",
                (
                    "The results are directionally believable for this sample, "
                    "but the system is not fully validated yet. More side-view "
                    "swing videos are needed to test stability, refine thresholds, "
                    "document failure cases, and eventually support ML-based "
                    "evaluation."
                ),
                "",
            ]
        )

        return "\n".join(report_lines)

    def save_report(self, report_text: str) -> None:
        """
        Save the evaluation report to disk.

        Args:
            report_text:
                Markdown report content.
        """
        self.output_report_path.parent.mkdir(parents=True, exist_ok=True)

        with open(self.output_report_path, "w", encoding="utf-8") as file:
            file.write(report_text)

    def run(self) -> str:
        """
        Run the full v1 evaluation and save the report.

        Returns:
            Path to the saved report as a string.
        """
        self.run_evaluation_checks()
        report_text = self.build_markdown_report()
        self.save_report(report_text)

        return str(self.output_report_path)